def get_folio(folio):
    folio = folio.strip()
    if not folio:
        return None
    conn = _connect()
    try:
        return _fetchone(conn, """
            SELECT f.id, f.folio, f.cliente, f.campana_id,
                   c.nombre AS campana, c.estado AS campana_estado
            FROM folios f
            LEFT JOIN campanas c ON c.id = f.campana_id
            WHERE f.folio = ?
        """, (folio,))
    finally:
        conn.close()


def get_revisiones(folio):
    """Todas las revisiones de un folio, de la más antigua a la más reciente."""
    folio = folio.strip()
    if not folio:
        return []
    conn = _connect()
    try:
        rev_rows = _fetchall(conn, """
            SELECT r.id, r.revision, r.evaluador, r.fecha, r.resultado
            FROM revisiones r JOIN folios f ON f.id = r.folio_id
            WHERE f.folio = ? ORDER BY r.revision ASC
        """, (folio,))
        revisiones = []
        for row in rev_rows:
            resp = _fetchall(conn,
                             "SELECT item_id, status, motivo FROM respuestas WHERE revision_id = ?",
                             (row["id"],))
            revisiones.append({
                "revision": row["revision"],
                "fecha": row["fecha"],
                "evaluador": row["evaluador"] or "",
                "resultado": row["resultado"],
                "respuestas": {
                    r["item_id"]: {"status": r["status"], "motivo": r["motivo"] or ""}
                    for r in resp
                },
            })
        return revisiones
    finally:
        conn.close()


def save_revision(folio, cliente, campana_id, evaluador, respuestas, resultado, fecha):
    """
    Da de alta o actualiza el folio (cliente/campaña) e inserta la nueva
    revisión, todo en una transacción. El número de revisión se calcula
    dentro de la misma transacción para que dos guardados simultáneos
    del mismo folio no se pisen.
    """
    folio = folio.strip()
    conn = _connect()
    try:
        if _is_postgres():
            with conn.cursor() as cur:
                # El upsert toma el candado de fila del folio dentro de la tx
                cur.execute(_sql("""
                    INSERT INTO folios (folio, cliente, campana_id) VALUES (?, ?, ?)
                    ON CONFLICT (folio) DO UPDATE
                        SET cliente = EXCLUDED.cliente, campana_id = EXCLUDED.campana_id
                    RETURNING id
                """), (folio, cliente, campana_id))
                folio_id = cur.fetchone()["id"]
                cur.execute(_sql(
                    "SELECT COALESCE(MAX(revision), 0) AS max_rev FROM revisiones WHERE folio_id = ?"
                ), (folio_id,))
                revision_num = cur.fetchone()["max_rev"] + 1
                cur.execute(_sql("""
                    INSERT INTO revisiones (folio_id, revision, evaluador, fecha, resultado)
                    VALUES (?, ?, ?, ?, ?) RETURNING id
                """), (folio_id, revision_num, evaluador, fecha, resultado))
                revision_id = cur.fetchone()["id"]
                cur.executemany(_sql(
                    "INSERT INTO respuestas (revision_id, item_id, status, motivo) VALUES (?, ?, ?, ?)"
                ), [(revision_id, item_id, r["status"], r.get("motivo", ""))
                    for item_id, r in respuestas.items()])
            conn.commit()
            return revision_num

        conn.execute("BEGIN IMMEDIATE")
        try:
            row = conn.execute("SELECT id FROM folios WHERE folio = ?", (folio,)).fetchone()
            if row:
                folio_id = row["id"]
                conn.execute("UPDATE folios SET cliente = ?, campana_id = ? WHERE id = ?",
                             (cliente, campana_id, folio_id))
            else:
                cur = conn.execute("INSERT INTO folios (folio, cliente, campana_id) VALUES (?, ?, ?)",
                                   (folio, cliente, campana_id))
                folio_id = cur.lastrowid
            row = conn.execute(
                "SELECT COALESCE(MAX(revision), 0) AS max_rev FROM revisiones WHERE folio_id = ?",
                (folio_id,)).fetchone()
            revision_num = row["max_rev"] + 1
            cur = conn.execute(
                "INSERT INTO revisiones (folio_id, revision, evaluador, fecha, resultado) VALUES (?, ?, ?, ?, ?)",
                (folio_id, revision_num, evaluador, fecha, resultado))
            revision_id = cur.lastrowid
            conn.executemany(
                "INSERT INTO respuestas (revision_id, item_id, status, motivo) VALUES (?, ?, ?, ?)",
                [(revision_id, item_id, r["status"], r.get("motivo", ""))
                 for item_id, r in respuestas.items()])
            conn.execute("COMMIT")
        except Exception:
            conn.execute("ROLLBACK")
            raise
        return revision_num
    finally:
        conn.close()


