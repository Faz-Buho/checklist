def _filtro_campana(filtro):
    """filtro: 'todas' | None (sin campaña) | id de campaña."""
    if filtro == "todas":
        return "", ()
    if filtro is None:
        return " AND f.campana_id IS NULL", ()
    return " AND f.campana_id = ?", (filtro,)


def get_revisiones_dashboard(filtro="todas"):
    """Una fila por revisión: folio, cliente, campana, revision, resultado, fecha, evaluador."""
    where, params = _filtro_campana(filtro)
    conn = _connect()
    try:
        return _fetchall(conn, f"""
            SELECT f.folio, f.cliente, c.nombre AS campana,
                   r.revision, r.resultado, r.fecha, r.evaluador
            FROM revisiones r
            JOIN folios f ON f.id = r.folio_id
            LEFT JOIN campanas c ON c.id = f.campana_id
            WHERE 1=1{where}
            ORDER BY f.folio, r.revision
        """, params)
    finally:
        conn.close()


def get_respuestas_dashboard(filtro="todas"):
    """Una fila por respuesta: folio, revision, item_id, status."""
    where, params = _filtro_campana(filtro)
    conn = _connect()
    try:
        return _fetchall(conn, f"""
            SELECT f.folio, r.revision, a.item_id, a.status
            FROM respuestas a
            JOIN revisiones r ON r.id = a.revision_id
            JOIN folios f ON f.id = r.folio_id
            WHERE 1=1{where}
        """, params)
    finally:
        conn.close()
