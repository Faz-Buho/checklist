def get_campanas(solo_abiertas=False):
    query = "SELECT id, nombre, estado FROM campanas"
    if solo_abiertas:
        query += " WHERE estado = 'abierta'"
    query += " ORDER BY nombre"
    conn = _connect()
    try:
        return _fetchall(conn, query)
    finally:
        conn.close()


def crear_campana(nombre):
    """Crea la campaña (o regresa la existente con ese nombre)."""
    nombre = nombre.strip()
    conn = _connect()
    try:
        existente = _fetchone(conn, "SELECT id FROM campanas WHERE lower(nombre) = lower(?)", (nombre,))
        if existente:
            return existente["id"]
        if _is_postgres():
            with conn.cursor() as cur:
                cur.execute(_sql("INSERT INTO campanas (nombre) VALUES (?) RETURNING id"), (nombre,))
                nuevo_id = cur.fetchone()["id"]
            conn.commit()
            return nuevo_id
        cur = conn.execute("INSERT INTO campanas (nombre) VALUES (?)", (nombre,))
        return cur.lastrowid
    finally:
        conn.close()


def cambiar_estado_campana(campana_id, estado):
    conn = _connect()
    try:
        if _is_postgres():
            with conn.cursor() as cur:
                cur.execute(_sql("UPDATE campanas SET estado = ? WHERE id = ?"), (estado, campana_id))
            conn.commit()
        else:
            conn.execute("UPDATE campanas SET estado = ? WHERE id = ?", (estado, campana_id))
    finally:
        conn.close()


